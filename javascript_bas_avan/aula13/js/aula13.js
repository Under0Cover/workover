function valor(x) {
    switch(x) {
        case 1:
            console.log('O número é igual a 1');
            break;
        case 2:
            console.log('O número é igual a 2');
            break;
        case 3:
            console.log('O número é igual a 3');
            break;
        case 4:
            console.log('O número é igual a 4');
            break;
        default:
            console.log('O número é maior ou igual a 5');
            break;
    }
}

valor(2);